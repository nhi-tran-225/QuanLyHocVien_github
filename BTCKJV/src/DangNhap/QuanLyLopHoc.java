    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(jtfSearch.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,"Bạn chưa nhập thông tin tìm kiếm");
        } else {
            try {
                DBConnection a = new DBConnection();
                Connection conn = a.getConnection();
                PreparedStatement sp = conn.prepareStatement("SELECT * FROM lop_hoc WHERE ma_lop_hoc =?");
                sp.setString(1,jtfSearch.getText());
                ResultSet rs = sp.executeQuery();
                //boolean ktr = rs.next();
                if(rs.next()==true) {
                    tfmlh.setText(rs.getString("ma_lop_hoc"));
                    cbxmkh.addItem(rs.getString("ma_khoa_hoc"));
                    cbxmhv.addItem(rs.getString("ma_hoc_vien"));
                    tfndk.setText(rs.getString("ngay_dang_ky"));
                    chkth.setText(rs.getString("tinh_trang"));

                }else {
                    JOptionPane.showMessageDialog(this,"Không tìm thấy lớp học");
                    jtfSearch.setText("");
                }

            } catch (Exception e) {
                System.out.println(e.toString());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void MoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MoiActionPerformed
        if(tfmlh.getText().isEmpty()
            ||tfndk.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập đủ thông tin");
        }
        else
        {
            try {
                DBConnection a = new DBConnection();
                Connection conn = a.getConnection();
                PreparedStatement sp = conn.prepareStatement("INSERT INTO lop_hoc(ma_lop_hoc,ma_khoa_hoc,ma_hoc_vien,ngay_dang_ky,tinh_trang)"
                    + "VALUES(?,?,?,?,?)");
                sp.setString(1,tfmlh.getText());
                sp.setString(2,cbxmkh.getSelectedItem().toString());
                sp.setString(3,cbxmhv.getSelectedItem().toString());
                sp.setString(4,tfndk.getText());
                boolean th = false;
                if(chkth.isSelected()) {
                    th = true;
                }
                sp.setBoolean(5, th);

                if(sp.executeUpdate()>0){
                    JOptionPane.showMessageDialog(this, "Thêm Thành Công!");
                    model.setRowCount(0);
                    taiDuLieu();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,"Lỗi");
                System.out.println(e.toString());
            }
        }
    }//GEN-LAST:event_MoiActionPerformed

