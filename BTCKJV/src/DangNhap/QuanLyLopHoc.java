    private void XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_XoaActionPerformed
        if(tfmlh.getText().isEmpty()
            ||tfndk.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn dữ liệu để xóa");
        }
        else
        {
            try {
                DBConnection a = new DBConnection();
                Connection conn = a.getConnection();
                PreparedStatement sp = conn.prepareStatement("DELETE lop_hoc WHERE ma_lop_hoc =?");
                if(jTable1.getColumnCount()==0){
                    JOptionPane.showConfirmDialog(this,"Bảng Trống");
                }else {
                    sp.setString(1,jTable1.getValueAt(jTable1.getSelectedRow(),0).toString());
                    if(JOptionPane.showConfirmDialog(this,"Bạn có muốn xóa lớp học này không?", "Xóa",
                        JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    sp.executeUpdate();
                    model.setRowCount(0);
                    taiDuLieu();
                }
            }

        } catch (Exception e) {
            System.out.println(e.toString());
        }
        }
    }//GEN-LAST:event_XoaActionPerformed

