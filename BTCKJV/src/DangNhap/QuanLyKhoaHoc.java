    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jtfSearch.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,"Bạn chưa nhập thông tin tìm kiếm");
        } else {
            try {
                DBConnection a = new DBConnection();
                Connection conn = a.getConnection();
                PreparedStatement sp = conn.prepareStatement("SELECT * FROM khoa_hoc WHERE ma_khoa_hoc =?");
                sp.setString(1,jtfSearch.getText());
                ResultSet rs = sp.executeQuery();
                //boolean ktr = rs.next();
                if(rs.next()==true) {
                    tfmkh.setText(rs.getString("ma_khoa_hoc"));
                    tftkh.setText(rs.getString("ten_khoa_hoc"));
                    tftgv.setText(rs.getString("ten_gv"));
                    tamta.setText(rs.getString("mo_ta"));
                    tftso.setText(rs.getString("tong_so_hoc_vien"));
                    tfnbd.setText(rs.getString("ngay_bat_dau"));
                    tfnkt.setText(rs.getString("ngay_ket_thuc"));
                    chkth.setText(rs.getString("tinh_trang"));
                }else {
                    JOptionPane.showMessageDialog(this,"Không tìm thấy khóa học");
                    jtfSearch.setText("");
                }

            } catch (Exception e) {
                System.out.println(e.toString());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

