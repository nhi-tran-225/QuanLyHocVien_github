    public void taiDuLieu() {
        try {
            DBConnection a = new DBConnection();
            Connection conn = a.getConnection();
            int so;
            Vector hang, cot;
            cot = new Vector();
            Statement ps = conn.createStatement();
            ResultSet rs = ps.executeQuery("SELECT * FROM khoa_hoc");
            ResultSetMetaData metadata = rs.getMetaData();
            so = metadata.getColumnCount();
            model.setColumnIdentifiers(new Object[]{
                "Mã Khóa Học", "Tên Khóa Học", "Tên Giảng Viên", "Mô tả", "Tổng số học viên", "Ngày Bắt Đầu", "Ngày Kết Thúc", "Tình Trạng"
            });

            while (rs.next()) {
                hang = new Vector();
                for (int i = 1; i <= so; i++) {
                    hang.addElement(rs.getString(i));

                }
                model.addRow(hang);
                jTable1.setModel(model);
            }

            jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if (jTable1.getSelectedRow() >= 0) {
                        tfmkh.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 0) + "");
                        tftkh.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1) + "");
                        tftgv.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2) + "");
                        tamta.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 3) + "");
                        tftso.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 4) + "");
                        tfnbd.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 5) + "");
                        tfnkt.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 6) + "");

                    }
                }
            });

        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }

